<h3><span class="glyphicons fire"><i></i></span>Damage Protection</h3>
<h4>Protection and Peace of Mind during your Vacation</h4>
<p><strong>Accidental Rental Damage Protection</strong> (sometimes also referred to as "Damage Deposit Waiver") is an insurance plan that provides coverage for accidental damages to your vacation rental property during your stay, in place of a security deposit.  Accidental Rental Damage Protection shields you from having to pay out for any damages you accidentally cause to your rental. </p>
<p>&nbsp;</p>
<h4>How do you obtain coverage?</h4>
<p>Automatically. <strong>Accidental Rental Damage Protection</strong> is automatically included when you book your reservation, and saves you from a big check, cash or tying up your credit card with a hefty authorization while you are on vacation.</p>
<p>&nbsp;</p>
<h4>What's covered?</h4>
<p>The <strong>Accidental Rental Damage Protection</strong> provides protection up to the amount of coverage per stay and is valid for the entire stay. With this plan, you and your traveling companions will not be obligated to pay for damage or theft to the contents of the rental unit you occupy.  And, the cost and repair of covered accidental damage will be handled between the rental property and the insurance company. </p>
<p>&nbsp;</p>
<h4>What's not covered?</h4>
<ul class="unstyled" style="margin-left: 17px;">
<li><span class="halflings remove"><i></i>Intentional acts of a guest and traveling companions;</span></li>
<li><span class="halflings remove"><i></i>Gross negligence or willful conduct;</span></li>
<li><span class="halflings remove"><i></i>Any loss, if a guest or traveling companions do not take reasonable and prudent measures to protect the residence;</span></li>
<li><span class="halflings remove"><i></i>Any loss, if the guest does not report the damage to residence rental management staff by the time of vacating the residence;</span></li>
<li><span class="halflings remove"><i></i>Theft without forced entry;</span></li>
<li><span class="halflings remove"><i></i>Damage or loss caused by any pet or other animal that was not allowed onto the premises under the Rental Agreement;</span></li>
<li><span class="halflings remove"><i></i>Normal wear and tear;</span></li>
</ul>

<h4>Protect your Vacation from Cancellation, Delays and Emergencies</h4>
<p><strong>Travel Protection</strong> (sometimes also referred to as "Cancellation Insurance") is an optional but recommended insurance plan that provides a wide array of significant levels of coverage for when things go wrong before and during your vacation.  Protect your trip with confidence!</p>
<p>&nbsp;</p>
<h4>What's covered?</h4>
<p><strong>Travel Protection</strong> is for sudden, unforeseen circumstances that may arise, that could force you to cancel or interrupt your vacation plans.  With this plan you protect the money you have invested in nonrefundable deposits and prepayments that can add up to hundreds or thousands of dollars.  This plan also covers accident and medical costs that in many instances may not be covered by health insurance.  While rare, this plan also covers losses from hurricanes, acts of terrorism and other events and situations, all outlined in detail in your policy. Here are some common scenarios where Travel Protection offers benefits.</p>
<p>&nbsp;</p>
<ul class="unstyled" style="margin-left: 17px;">
<li><span class="halflings ok"><i></i>Trip Cancellations due to unforeseen circumstances</span></li>
<li><span class="halflings ok"><i></i>Trip Interruptions</span></li>
<li><span class="halflings ok"><i></i>Certain Travel &amp; Baggage Delay</span>s</li>
<li><span class="halflings ok"><i></i>Certain Medical &amp; Dental Expenses</span></li>
<li><span class="halflings ok"><i></i>Medical Evacuation &amp; Repatriation</span></li>
</ul>
